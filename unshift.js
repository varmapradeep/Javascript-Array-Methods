//Syntax
// array.unshift(item1, item2, ..., itemX);

//Example

let fruits = ["Mango", "Apple", "Orange", "Grapes", "Cherry", "Blueberry"];
console.log(fruits.unshift("Pinapple","Pear")); // "8"

console.log(fruits); // change the original array

//Output: ["Pinapple","Pear","Apple","Orange","Grapes","Cherry","Blueberry"]

