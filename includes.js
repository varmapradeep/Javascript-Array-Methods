//Syntax
// array.includes(valueToFind, fromIndex?)     


// Example 1: Using includes() with strings
let message = "Hello JavaScript";

console.log(message.includes("Java"));   // Output: true
console.log(message.includes("Python")); // Output: false
  

// Example 2: Using includes() with numbers
let numbers = [1, 2, 3, 4, 5];
console.log(numbers.includes(3));    // Output: true
console.log(numbers.includes(10));   // Output: false










