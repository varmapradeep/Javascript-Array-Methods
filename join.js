//Syntax
array.join(separator)           

//Example

let fruits = ["Mango", "Apple", "Orange", "Grapes"];
console.log(fruits.join()); 
//Output: "Mango,Apple,Orange,Grapes"

console.log(fruits.join("")); 
//Output: "MangoAppleOrangeGrapes"

console.log(fruits.join("-")); 
//Output: "Mango-Apple-Orange-Grapes"


