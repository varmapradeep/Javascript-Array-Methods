//Syntax
string.lastIndexOf(searchValue, startIndex(position));

array.lastIndexOf(searchElement, fromIndex(position));    

//Examples

let sentence = "I love coding and I love JavaScript";
console.log(sentence.lastIndexOf("coding"));   // Output: 7
console.log(sentence.lastIndexOf("love"));  // Output: 20

let numbers = [1, 2, 3, 2, 1];
console.log(numbers.lastIndexOf(1));    // Output: 4
console.log(numbers.lastIndexOf(2));   // Output: 3 








